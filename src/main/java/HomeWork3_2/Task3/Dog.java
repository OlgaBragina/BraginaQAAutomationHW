package Task3;

public class Dog {
    public String name;
    public int age;

    public Dog (String name, int age)
    {
        this.age=age;
        this.name=name;
    }

    public Dog(){

    }
        public String getName ()
        {
            return name;
        }
        public int getAge ()
        {
            return age;
        }
    public void setName(String name)
    {
        this.name = name;
    }
    public void setAge(int age)
    {
        this.age = age;
    }




}
